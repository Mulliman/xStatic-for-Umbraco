/**
 * @fileoverview Dashboard Constants
 *
 * Defines aliases for Notes dashboards.
 * Dashboards provide summary views and quick actions.
 *
 * Skills demonstrated: umbraco-dashboard
 */

/**
 * Alias for the Notes welcome dashboard.
 *
 * The welcome dashboard appears when entering the Notes section.
 * It shows recent notes and quick action buttons.
 *
 * @constant {string}
 */
export const NOTES_DASHBOARD_ALIAS = "Notes.Dashboard";

/**
 * Alias for the Notes browse dashboard.
 *
 * The browse dashboard provides a grid view of notes and folders,
 * used when viewing folder contents.
 *
 * @constant {string}
 */
export const NOTES_BROWSE_DASHBOARD_ALIAS = "Notes.Dashboard.Browse";
